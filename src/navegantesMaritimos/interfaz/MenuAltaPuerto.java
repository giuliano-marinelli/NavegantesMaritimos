    private void comboBoxPaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxPaisActionPerformed
        Pais p = (Pais) comboBoxPais.getSelectedItem();
        try {
            labelBandera.setIcon(new ImageIcon(getClass().getResource("../recursos/banderas/" + p.getAlias() + ".png")));
        } catch (Exception e) {
            labelBandera.setIcon(new ImageIcon(getClass().getResource("../recursos/banderas/null.png")));
        }

    }//GEN-LAST:event_comboBoxPaisActionPerformed

    private void buttonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCrearActionPerformed
        if (Validador.cantCarac(textFieldNombre, 5, 50)
                && Validador.enteroRango(textFieldCantDarsenas, 1, 1000)) {
            String nombre = textFieldNombre.getText();
            String pais = comboBoxPais.getSelectedItem().toString();
            int cantDarsenas = Integer.parseInt(textFieldCantDarsenas.getText());
            boolean correcto = nav.altaPuerto(nombre, pais, cantDarsenas);
            if (correcto) {
                JOptionPane.showMessageDialog(this, "El puerto " + nombre + " se ha dado de alta correctamente.", "Alta de puerto exitosa", 1);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se ha podido crear el puerto. Es posible que ya exista un puerto con el nombre ingresado. Intentelo nuevamente.", "Error", 0);
            }
        }
    }//GEN-LAST:event_buttonCrearActionPerformed

