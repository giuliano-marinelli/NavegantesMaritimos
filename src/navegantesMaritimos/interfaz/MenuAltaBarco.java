    private void buttonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCrearActionPerformed
        if (Validador.cantCarac(textFieldNombre, 5, 50)) {
            String nombre = textFieldNombre.getText();
            String tipo = comboBoxTipo.getSelectedItem().toString();
            String pais = comboBoxPais.getSelectedItem().toString();
            boolean correcto = nav.altaBarco(nombre, tipo, pais);
            if (correcto) {
                nav.ponerBarco(nombre, nombrePuerto);
                JOptionPane.showMessageDialog(this, "El barco " + nombre + " se ha dado de alta correctamente.", "Alta de barco exitosa", 1);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se ha podido crear el barco. Es posible que ya exista un barco con el nombre ingresado. Intentelo nuevamente.", "Error", 0);
            }
        }
    }//GEN-LAST:event_buttonCrearActionPerformed

